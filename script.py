    "    async def run(self):\n",
    "        if not os.path.exists('reports'): os.makedirs('reports')\n",
    "        df_input = pd.read_excel(self.client_file, header=5)\n",
    "        \n",
    "        # Автоопределение колонок\n",
    "        name_col = next(c for c in df_input.columns if 'Наименование' in str(c))\n",
    "        inn_col = next(c for c in df_input.columns if 'ИНН' in str(c))\n",
    "        \n",
    "        companies = df_input[[name_col, inn_col]].dropna()\n",
    "        companies.columns = ['name', 'inn']\n",
    "\n",
    "        async with async_playwright() as p:\n",
    "            browser = await p.chromium.launch(headless=self.headless)\n",
    "            for _, row in tqdm(companies.iterrows(), total=len(companies)):\n",
    "                status, pubs = await self.check_bankruptcy(str(row['inn']).strip(), browser)\n",
    "                self.results.append({'ИНН': row['inn'], 'Наименование': row['name'], 'Банкротство': status, 'Публикации': pubs})\n",
    "                await asyncio.sleep(self.delay)\n",
    "            await browser.close()\n",
    "\n",
    "        df_today = pd.DataFrame(self.results)\n",
    "        df_today.to_excel(self.output_file, index=False)\n",
    "\n",
    "        # Сравнение\n",
    "        if os.path.exists(self.history_file):\n",
    "            with open(self.history_file, 'r', encoding='utf-8') as f:\n",
    "                history = json.load(f)\n",
    "        else:\n",
    "            history = {}\n",
    "\n",
    "        dates = sorted(history.keys())\n",
    "        change_status = \"Базовый\"\n",
    "        if dates:\n",
    "            last_data = history[dates[-1]]['data']\n",
    "            # Сравниваем только значимое поле 'Банкротство'\n",
    "            change_status = \"Нет изменений\" if [r['Банкротство'] for r in last_data] == [r['Банкротство'] for r in self.results] else \"⚠️ Есть изменения\"\n",
    "\n",
    "        history[self.today_str] = {'status': change_status, 'file': self.output_file, 'data': self.results}\n",
    "        \n",
    "        with open(self.history_file, 'w', encoding='utf-8') as f:\n",
    "            json.dump(history, f, ensure_ascii=False)\n",
    "        \n",
    "        self.generate_dashboard(history)\n",
