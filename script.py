    "        if match:\n",
    "            result['number'] = match.group(1)\n",
    "            result['date'] = match.group(2)\n",
    "        for phrase in self.key_phrases:\n",
    "            if phrase.lower() in text.lower():\n",
    "                result['title'] = phrase\n",
    "                break\n",
    "        return result\n",
