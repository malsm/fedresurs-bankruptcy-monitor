    "    async def find_company_id(self, inn: str, browser) -> str:\n",
    "        page = await browser.new_page()\n",
    "        try:\n",
    "            await page.goto(f\"https://fedresurs.ru/entities?searchString={inn}\", wait_until='networkidle', timeout=60000)\n",
